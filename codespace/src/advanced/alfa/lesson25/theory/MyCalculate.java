package advanced.alfa.lesson25.theory;

public class MyCalculate {
    public int calcSum(int a, int b) {
        return a + b;
    }

    public int calcSub(int a, int b) {
        return a - b;
    }

    public int calcMult(int a, int b) {
        return a * b;
    }

    public int calcDiv(int a, int b) {
        return a / b;
    }
}
